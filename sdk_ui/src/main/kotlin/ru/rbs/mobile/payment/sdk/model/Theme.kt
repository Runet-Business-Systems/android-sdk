package ru.rbs.mobile.payment.sdk.model

/**
 * Возможные варианты использования тем оформления.
 */
enum class Theme {

    /**
     * Текущая тема приложения.
     */
    DEFAULT,

    /**
     * Светлая тема.
     */
    LIGHT,

    /**
     * Темная тема.
     */
    DARK,

    /**
     * В соответствии с темой системы.
     */
    SYSTEM
}
